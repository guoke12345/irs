package com.framework.core.web.ui;

public class OptionsInt {

	private int key;

	private String value;

	public OptionsInt() {

	}

	public OptionsInt(int key, String value) {
		this.key = key;
		this.value = value;
	}

	public int getKey() {
		return key;
	}

	public void setKey(int key) {
		this.key = key;
	}

	public String getValue() {
		return value;
	}

	public void setValue(String value) {
		this.value = value;
	}

}
